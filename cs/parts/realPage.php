            <h2 id="BachelorTitle">bakalářský studijní program</h2>
            <p>
                Katedra umění, Fakulty umění a architektury Technické univerzity v
                Liberci garantuje bakalářský studijní program Výtvarné umění - Tvorba ve veřejném prostoru.
                Předmětem studia je tvorba ve veřejném prostoru. Studijní program poskytuje teoretické znalosti a
                praktické dovednosti v tvorbě ve veřejně sdíleném prostoru. Základem studia je průzkum, analýza a
                utváření vztahů mezi designem, uměním a veřejně sdílenými prostory utvářenými občanskou
                společností. Studium se zabývá způsoby, postupy a metodami tvorby ve veřejném prostoru,
                podmínkami jejich recepce a kritiky. Cílem studia je poskytnout studentům (návrhářům prostředí)
                vysokoškolské vzdělání, obecnější rozhled a kritické myšlení v oblasti tvorby veřejného prostoru.
            </p>
            <p>
                Studijní program Výtvarné umění – Tvorba ve veřejném prostoru se zabývá navrhováním na hranici a
                mimo hranice jednotlivých uměleckých oborů a médií. Tvorba současných vztahů mezi těmito a
                dalšími obory je bezprostředně spojena s koncepty a performativy veřejnosti, veřejných (občanských)
                aktivit a iniciativ, tedy se školenou tvorbou i neškolenou participací zaměřenou na pěstování
                společenského duchovního i fyzického prostoru, který je sdílen lidmi bez předdefinování jejich
                rozdílů. Studijní program navazuje na současné tendence zásahů do veřejného prostoru a připravuje
                odborníky, kteří budou aktivně kvalitu veřejného prostoru ovlivňovat.
            </p>
            <span><i class="fas fa-user-graduate"></i></span>
            <h3 class="thirdTitle">profil absolventa</h3>
            <p>
                Absolventi programu Výtvarné umění se uplatní jako návrháři objektů a prostředí ve veřejném prostoru ve všech jeho
                kontextech. Mohou také působit jako samostatní umělci a návrháři v oblastech volného či užitého
                umění, jako členové a spolupracovníci uměleckých, designérských, architektonických a urbanistických
                ateliérů a skupin, ale také jako konzultanti občanských iniciativ a komunitních sdružení. Absolventi
                tvoří ve veřejném prostoru díla (objekty instalace a prostředí) navrhovaná bez omezení klasickými a
                moderními uměleckými, architektonickými a urbanistickými druhy a médii. Mohou zahrnovat tvorbu
                2D i 3D fyzických i digitálních objektů, instalací, prostředí, ale také různé návrhy intervencí do
                urbánních, krajinných a smíšených veřejných prostor a míst a konečně také rozmanité způsoby
                individuální a komunitní participace občanské veřejnosti na jejich vzniku.
            </p>
            <p>
                Obecněji i cíleně vzdělávaný absolvent bude schopen v praxi propojovat různé expertní přístupy a sdílet tvůrčí procesy
                s profesemi často izolovanými v profesních komorách, anebo uměleckých spolcích/skupinách. Bude
                schopen v praxi i v teorii kriticky přehodnotit vztahy těchto profesí k veřejným zájmům a také
                zohlednit různá napětí mezi vědou, technikou a uměním na poli veřejně sdíleného prostoru.
                Absolvent bakalářského studijního programu získá titul obdobný jako na ostatních vysokých školách
                uměleckého směru – bakalář umění (zkratka BcA.)
            </p>
