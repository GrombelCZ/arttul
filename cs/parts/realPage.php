            <h2 id="MasterTitle">magisterský studijní program</h2>
            <p>
                Cíle studia ve studijním programu Předmětem studia je především volná umělecká tvorba a
                inovativní náhled na tvorbu ve veřejném prostoru. Program Výtvarné umění – Tvorba ve veřejném
                prostoru poskytuje především teoretické znalosti a dovednosti v umělecké tvorbě a rozvádí její
                možnosti a dopady na veřejný prostor. Studium akcentuje schopnost zevrubného průzkumu a analýzy
                vztahů mezi uměním, privátním a veřejně sdílenými prostorem a všemi okolnostmi jejich vzájemného
                působení. Zabývá se strategiemi a funkcí umění ve veřejném prostoru, podmínkami jejich recepce a
                kritiky. Zahrnuje poznatky o sociální, kulturní a umělecké infrastruktuře komunitních a společenských
                prostředí.
            </p>
            <p>
                Program podporuje tvůrčí a inovativní myšlení v oblasti utváření, recepce a kritiky
                umělecké tvorby ve veřejném prostoru a preferuje multidisciplinární přístup k tvůrčí praxi s aktivním
                využitím veškerých dostupných médií. Tvorba ve veřejném prostoru se zabývá mapováním oblasti
                průniku uměleckých oborů zastoupených na Fakultě umění a architektury Technické univerzity v
                Liberci (umění, architektura, urbanismus) formou navrhování a realizací samostatných uměleckých
                koncepcí a podniků v prostoru umění, společnosti a médií. Je bezprostředně spojena se školenou
                tvorbou a participací zaměřenou na pěstování společenského duchovního i fyzického prostoru, který
                je sdílen a poskytuje inspiraci svému okolí. Zachází s koncepty rozvíjející veřejné (občanské) aktivity a
                iniciativy.
            </p>
            <span><i class="fas fa-user-graduate"></i></span>
            <h3 class="thirdTitle">profil absolventa</h3>
            <p>
                Program navržen tak, aby absolvent byl nejen prakticky i umělecky zdatnou osobností využívající kvalifikovaně
                nové technologie se schopností reagovat nejen na běžná zadání z oblasti umění, ale i osobností, která
                je schopna splnit požadavky na zadání ve vztahu k&nbsp;veřejnému prostoru. Absolvent je aktivním
                formotvorným článkem utvářejícím informační pole veřejného prostoru. Absolvent by měl být zcela
                samostatný talentovaný tvůrce nacházející uplatnění především v oblastech volného umění s
                přesahem k obecné i speciální problematice prostoru jako takového.
            </p>
            <p>
                Program vyhovuje těm, kteří
                mají dovednosti ve vizuální praxi, performanci, videu, zvukovém a kreativním umění, architektuře,
                módě a kurátorských a kulturních oborech s ambicemi rozvíjet své dovednosti v rámci své dosavadní
                kariéry nebo se pohybovat v umělecké praxi na veřejnosti i sociálním prostoru. Magistr umění (umění
                ve veřejném prostoru) rozvíjí dialog mezi společností a veřejným prostorem. Jeho práce skýtá prostor
                k hlubokému zamyšlení nad tím, jak a proč fungovat v rámci umělecké a širší společenské komunity.
                Absolvent magisterského studijního programu získá titul určený školám uměleckého směru – magistr
                umění (zkratka MgA.).
            </p>
