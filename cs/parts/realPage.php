        <h1 id="WeTitle" class="firstTitle">O NÁS</h1>
        <p>
            <strong>Základem studia</strong> na Katedře umění <strong>je průzkum, analýza a utváření</strong> vztahů mezi designem, uměním a veřejně sdílenými prostory.
            Pedagogové na Katedře umění podporují tvůrčí a inovativní myšlení v oblasti utváření, <strong>recepce a kritiky</strong> umělecké tvorby
            ve veřejném prostoru. Preferují multidisciplinární přístup k tvůrčí praxi s aktivním využitím veškerých dostupných médií.
            Ty mohou zahrnovat tvorbu 2D i 3D <strong>fyzických i digitálních</strong> objektů, instalací, prostředí, ale také návrhy intervencí do
            urbánních, krajinných a smíšených veřejných prostor a míst a rozmanité způsoby individuální a komunitní participace občanské
            veřejnosti na jejich vzniku.
        </p>
        <p>
            Studijní obor Výtvarné umění – <strong>Tvorba ve veřejném prostoru</strong>, který katedra umění zaštiťuje, navazuje na současné tendence
            dočasných i trvalých zásahů do veřejného prostoru a připravuje odborníky, kteří budou aktivně kvalitu veřejného prostoru ovlivňovat.
        </p>
