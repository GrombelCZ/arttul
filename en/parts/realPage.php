            <h2 id="MasterTitle">master's degree program</h2>
            <p>
                The subject of the study is mainly free artistic creation and an innovative view of creation in public space.
                The Fine Arts - Creation in Public Space programme provides primarily theoretical knowledge and skills in artistic
                creation and expands on its possibilities and impacts on public space. The study emphasizes the ability of thorough
                research and analysis of the relationships between art, private and public shared spaces and all the circumstances
                of their interaction. It deals with the strategies and functions of art in public space, the conditions of their
                reception and criticism. Includes knowledge of the social, cultural and artistic infrastructure of community and
                public environments.
            </p>
            <p>
                The program supports creative and innovative thinking in the field of formation, reception and criticism of artistic
                creation in public space and prefers a multidisciplinary approach to creative practice with the active use of all
                available media. Creation in public space deals with mapping the intersection of artistic disciplines represented
                at the Faculty of Arts and Architecture of the Technical University of Liberec (art, architecture, urbanism) in the
                design and implementation of independent artistic concepts and projects in the field of art, society and media.
                It is directly connected with trained work and participation focused on the cultivation of social, spiritual and
                physical space, which is shared and provides inspiration to its surroundings. It deals with concepts developing
                public (civic) activities and initiatives.
            </p>
            <span><i class="fas fa-user-graduate"></i></span>
            <h3 class="thirdTitle">profile of the graduate</h3>
            <p>
                The program is designed so that the graduate is not only a practically and artistically capable personality using
                qualified new technologies with the ability to respond not only to common assignments in the field of art, but
                also a personality who is able to meet requirements for assignment in relation to public space. The graduate
                is an active form-forming article which design the information field of public space. The graduate should
                be a completely independent talented creator finding employment mainly in the fields of free art with an
                overlap to general and special issues of space as such.
            </p>
            <p>
                The program is aimed at personalities who have skills in visual practice, performance, video, sound and creative arts,
                architecture, fashion and curatorial and cultural disciplines with ambitions to develop their skills within their
                current career or to move in artistic practice in public and social space. The Master of Arts (Creation in Public Space)
                develops a dialogue between society and public space. His work provides a space for in-depth reflection on how and why
                to function within the artistic and wider social community. The graduate of the master's study program will obtain a degree
                intended for schools of arts - master of arts (abbreviation MgA.).
            </p>
