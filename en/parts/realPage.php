        <h1 id="WeTitle" class="firstTitle">ABOUT US</h1>
        <p>
            <strong>The basis of studies</strong> at the Department of Arts <strong>is research, analysis and the formation</strong> of relationships between art, design and public spaces.
            Educators at the Department of Arts support creative and innovative thinking in the field of creation, <strong>reception and critique</strong> of artistic creation
            in public space. They prefer a multidisciplinary approach to creative practice with the active use of all available media. These may include the
            creation of 2D and 3D <strong>physical and digital</strong> objects, installations, environments, but also drafts for interventions in urban, landscape and mixed
            public spaces and various ways of individual and community participation of the civil public in their creation.
        </p>
        <p>
            Study Programme Fine Arts - <strong>Creation in Public Space</strong>, which is provided by the Department of Arts, follows the current tendencies of temporary and
            permanent interventions in public space and prepares experts who will actively influence the quality of public space.
        </p>
