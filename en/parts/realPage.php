        <h1 id="StudyTitle" class="firstTitle">STUDIES</h1>
        <div class="content">
            <h2 id="BachelorTitle">bachelor study programme</h2>
            <p>
                The Department of Arts, Faculty of Arts and Architecture of the Technical University of Liberec guarantees the bachelor's study program
                Fine Arts - Creation in Public Space. The subject of the study is creation in public space. The study program provides theoretical
                knowledge and practical skills in creation in a publicly shared space. The basis of the study is the research, analysis and formation
                of relationships between design, art and publicly shared spaces formed by civil society. The study deals with the ways, procedures
                and methods of creation in public space, the conditions of their reception and criticism. The main goal of the study is to provide
                students with space for the development of their own creative potential, insight and critical thinking in the field of public space creation.
            </p>
            <p>
                The study program Fine Arts - Creation in Public Space deals with design on the border and outside the borders of individual artistic fields
                and media. The creation of current relations between these and other fields is directly connected with the concepts and performances of the
                community, public (civic) activities and initiatives. The study program follows the current tendencies of interventions in public space and
                prepares experts who will actively influence the quality of public space.
            </p>
            <span><i class="fas fa-user-graduate"></i></span>
            <h3 class="thirdTitle">profile of the graduate</h3>
            <p>
                Graduates of the program will work as designers of objects and environments in public space in all its contexts.
                They can also work as independent artists in the fields of free or applied arts, as members and collaborators
                of art, design, architectural and urban studios and groups, but also as consultants for civic initiatives and
                community associations. Graduates create works in the public space (objects of installation and environment)
                designed without restrictions by classical and modern artistic, architectural and urban types and media. They
                can include the creation of 2D and 3D physical and digital objects, installations, environments, but also various
                drafts for interventions in urban, landscape and mixed public spaces and places, and finally various ways of
                individual and community participation of the civic public in their creation.
            </p>
            <p>
                Generally, even a purposefully educated graduate will be able to combine various expert approaches in practice and
                share creative processes with professions often isolated in professional chambers or artistic associations/groups.
                He will be able to critically reconsider the relationship of these professions to the public interest in practice
                and in theory, and also to take into account the various tensions between science, technology and art in the field
                of public space. The graduate of the bachelor's study program will obtain a degree similar to other art universities -
                bachelor of arts (abbreviation BcA.)
            </p>
        </div>
